    <h4><a name="crater">What are Transient and Final Crater Diameters?</a></h4>
    <p>When the projectile strikes the surface, it ejects rock from the surface, resulting in a crater. Shortly after
      the impact, the
      ground around the initially formed crater begins to collapse inward. </p>
    <p>The Transient Crater Diameter is the distance from rim to rim of the crater that forms immediately after the
      impact, before the
      collapse begins. </p>
    <p>The Final Crater Diameter is the distance from rim to rim of the crater once collapse has completed.
    </p>


