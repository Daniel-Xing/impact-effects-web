    <h4><a name="crater">What are Transient and Final Crater Diameters?</a></h4>
    <p>When the projectile strikes the surface, it ejects rock from the surface, resulting in a crater. Shortly after
      the impact, the
      ground around the initially formed crater begins to collapse inward. </p>
    <p>The Transient Crater Diameter is the distance from rim to rim of the crater that forms immediately after the
      impact, before the
      collapse begins. </p>
    <p>The Final Crater Diameter is the distance from rim to rim of the crater once collapse has completed.
    </p>


    <h4><a name="thermal">What is Thermal Radiation?</a></h4>
    <p>Terrestrial meteoroid impacts produce a cloud of hot vapor, termed the "fireball," which expands away from the
      impact point. The
      bulk of the thermal energy from this cloud is emitted when the fireball has expanded and cooled to a surface
      temperature of about 3000
      K. </p>
    <p>Thermal Exposure is the thermal energy per unit area that arrives at the user specified distance. To provide the
      user with a
      reference as to how damaging the thermal radiation is, several text descriptions of the damage are provided if the
      thermal energy is
      great enough to cause the described damage.
    </p>
    <dl>These descriptions include:
      <dd>Clothing ignites
      </dd>
      <dd>Much of the body suffers third degree burns
      </dd>
