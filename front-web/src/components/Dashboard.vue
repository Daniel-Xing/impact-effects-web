    <h4><a name="ejecta">What is ejecta?</a></h4>
    <p>Debris excavated during the impact is deposited on the Earth's surface around the
      impact crater. This debris is called ejecta.
    </p>
    <p>Ejecta is formed of particles (rock fragments) of a range of sizes. The average fragment size that we report is
      the average particle size at the given distance, but a range of particle sizes will land at any given location.
      Average fragment size decreases with distance from the crater, because smaller fragments tend to be ejected faster
      and from closer to the impact point.</p>
    <p>When the ejecta lands it forms a layer of ejecta called an ejecta deposit or ejecta blanket. The average ejecta
      thickness is greatest at the <a target="blank"
        href="http://www.lpl.arizona.edu/~marcus/craterglos.html#transient">
        transient crater</a> rim and decreases as one over the distance from the rim cubed. Near the crater rim, this
      deposit is relatively thick and "continuous". It is often referred to as the continuous ejecta blanket. Further
      away, the deposit is much thinner and may be patchy (discontinuous).</p>
    <p>At a given distance from the crater, the ejecta deposit may have a range of thicknesses, depending on the
      direction, particularly at ranges where the ejecta is discontinuous. Hence the estimate of ejecta thickness we
      give is an "average" around the crater at the given distance.</p>


    <h4><a name="airblast">What is the air blast?</a></h4>
    <p>The energy due to the impact causes a distortion in the air. This distortion travels in the form of a wave. If
      the energy of the
      impact is very high, the wave may initially be a shock wave, travelling at a velocity greater than the speed of
      sound in air. The wave
      eventually decays into a sound wave travelling at 300 m/s (671 mph).
    </p>
    <p>Peak overpressure is a measure of how much the pressure in the blast wave exceeds the atmospheric pressure of
      10<sup>5</sup>
      Pa (1 bar).
      The air blast caused by the impact can cause a great deal of damage.
    </p>
    <dl>The damage due to the air blast may include the following:
      <dd>Multistory wall-bearing buildings will collapse.
      </dd>
      <dd>Multistory wall-bearing buildings will experience severe cracking and interior partitions will be blown down.
      </dd>
      <dd>Wood frame buildings will almost completely collapse.
      </dd>
      <dd>Interior partitions of wood frame buildings will be blown down. Roof will be severely damaged.
      </dd>
      <dd>Multistory steel-framed office-type buildings will suffer extreme frame distortion, incipient collapse.
      </dd>
      <dd>Highway truss bridges will collapse.
      </dd>
      <dd>Highway truss bridges will suffer substantial distortion of bracing.
      </dd>
      <dd>Highway girder bridges will collapse.
      </dd>
      <dd>Cars and trucks will be largely displaced and grossly distorted and will require rebuilding before use.
      </dd>
      <dd>Cars and trucks will be overturned and displaced, requiring major repairs.
      </dd>
      <dd>Glass windows will shatter
      </dd>
      <dd>About 30 percent of trees blown down; remainder have some branches and leaves blown off
      </dd>
      <dd>Up to 90 percent of trees blown down; remainder stripped of branches and leaves.
      </dd>
    </dl>

